# Определяем базовый класс — Двигатель
class Двигатель:
    """Базовый класс для всех двигателей. Содержит общие параметры."""
    
    def __init__(self, угол_поворота=0.0, скорость_вращения=0.0, ускорение=0.0, мощность=0.0):
        """
        Инициализирует двигатель.
        Параметры по умолчанию: нулевые значения.
        Мощность — важный параметр для сравнения.
        """
        self.угол_поворота = угол_поворота
        self.скорость_вращения = скорость_вращения
        self.ускорение = ускорение
        self.мощность = мощность  # Ватты или другая единица — для сравнения

    def __str__(self):
        """Возвращает человеко-читаемую строку о двигателе."""
        return f"Двигатель: угол={self.угол_поворота}, скорость={self.скорость_вращения}, мощность={self.мощность}"

    def __repr__(self):
        """Возвращает строку, которая может быть использована для создания объекта."""
        return f"Двигатель(угол_поворота={self.угол_поворота}, скорость_вращения={self.скорость_вращения}, мощность={self.мощность})"

    # Перегрузка операторов сравнения по мощности
    def __eq__(self, other):
        """Проверяет, равны ли два двигателя по мощности."""
        if isinstance(other, Двигатель):
            return self.мощность == other.мощность
        return False

    def __lt__(self, other):
        """Проверяет, меньше ли текущий двигатель по мощности."""
        if isinstance(other, Двигатель):
            return self.мощность < other.мощность
        return False

    def __gt__(self, other):
        """Проверяет, больше ли текущий двигатель по мощности."""
        if isinstance(other, Двигатель):
            return self.мощность > other.мощность
        return False


# Определяем класс-потомок — ВращательныйДвигатель
class ВращательныйДвигатель(Двигатель):
    """Класс для двигателей, которые вращаются (например, в роботах)."""
    
    def __init__(self, угол_поворота=0.0, скорость_вращения=0.0, ускорение=0.0, мощность=0.0, ось="Z"):
        """
        Наследует все параметры от Двигатель, добавляет специфический параметр — ось вращения.
        По умолчанию вращается вокруг оси Z.
        """
        # Вызываем конструктор родительского класса
        super().__init__(угол_поворота, скорость_вращения, ускорение, мощность)
        self.ось = ось  # Например: "X", "Y", "Z"

    def __str__(self):
        """Переопределяем строковое представление, чтобы показать ось."""
        base_str = super().__str__()
        return f"{base_str}, ось={self.ось}"

    def __repr__(self):
        """Переопределяем repr, чтобы включить ось."""
        return f"ВращательныйДвигатель(угол_поворота={self.угол_поворота}, скорость_вращения={self.скорость_вращения}, мощность={self.мощность}, ось='{self.ось}')"


# Определяем класс-потомок — СинхронныйСервопривод
class СинхронныйСервопривод(ВращательныйДвигатель):
    """Класс для точных приводов, используемых в роботах. Добавляет частоту и тип управления."""
    
    def __init__(self, угол_поворота=0.0, скорость_вращения=0.0, ускорение=0.0, мощность=0.0, ось="Z", частота=50.0, тип_управления="PID"):
        """
        Наследует от ВращательныйДвигатель, добавляет частоту и тип управления.
        Частота — в Гц, тип_управления — например, "PID", "PWM".
        """
        # Вызываем конструктор родителя
        super().__init__(угол_поворота, скорость_вращения, ускорение, мощность, ось)
        self.частота = частота
        self.тип_управления = тип_управления

    def __str__(self):
        """Добавляем частоту и тип управления в строковое представление."""
        base_str = super().__str__()
        return f"{base_str}, частота={self.частота} Гц, управление={self.тип_управления}"

    def __repr__(self):
        """Добавляем частоту и тип управления в repr."""
        return f"СинхронныйСервопривод(угол_поворота={self.угол_поворота}, скорость_вращения={self.скорость_вращения}, мощность={self.мощность}, ось='{self.ось}', частота={self.частота}, тип_управления='{self.тип_управления}')"


# Класс для модели шестизвенного манипулятора
class ШестизвенныйМанипулятор:
    """Модель робота-манипулятора, состоящего из шести сервоприводов."""
    
    def __init__(self, сервоприводы=None):
        """
        Принимает список из 6 сервоприводов.
        Если не передан — создаёт 6 стандартных сервоприводов.
        """
        if сервоприводы is None:
            # Создаём 6 одинаковых сервоприводов по умолчанию
            self.сервоприводы = [
                СинхронныйСервопривод(мощность=100.0 + i*10, ось=f"Ось{i+1}")
                for i in range(6)
            ]
        else:
            # Проверяем, что передали ровно 6 элементов
            if len(сервоприводы) != 6:
                raise ValueError("Манипулятор должен содержать ровно 6 сервоприводов.")
            self.сервоприводы = сервоприводы

    def __str__(self):
        """Возвращает краткое описание манипулятора."""
        return f"Шестизвенный манипулятор с {len(self.сервоприводы)} сервоприводами."

    def __repr__(self):
        """Возвращает полное представление манипулятора."""
        return f"ШестизвенныйМанипулятор(сервоприводы={self.сервоприводы})"

    # Перегрузка оператора сложения (+) для перемещения манипулятора
    def __add__(self, вектор_перемещения):
        """
        Перемещает манипулятор в пространстве.
        Вектор перемещения — это кортеж (dx, dy, dz).
        Для простоты мы просто меняем углы поворота всех сервоприводов на значение dx.
        В реальной жизни это было бы сложнее — но для учебной задачи подойдёт.
        """
        if not isinstance(вектор_перемещения, tuple) or len(вектор_перемещения) != 3:
            raise TypeError("Вектор перемещения должен быть кортежем из трёх чисел (dx, dy, dz).")

        # Создаём новый манипулятор с изменёнными углами
        новые_сервоприводы = []
        for сервопривод in self.сервоприводы:
            # Копируем сервопривод и меняем его угол на dx
            новый_сервопривод = СинхронныйСервопривод(
                угол_поворота=сервопривод.угол_поворота + вектор_перемещения[0],  # Изменяем угол на dx
                скорость_вращения=сервопривод.скорость_вращения,
                ускорение=сервопривод.ускорение,
                мощность=сервопривод.мощность,
                ось=сервопривод.ось,
                частота=сервопривод.частота,
                тип_управления=сервопривод.тип_управления
            )
            новые_сервоприводы.append(новый_сервопривод)

        # Возвращаем новый манипулятор
        return ШестизвенныйМанипулятор(новые_сервоприводы)


