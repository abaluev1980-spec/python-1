# Импортируем модуль abc — он позволяет создавать абстрактные классы и методы.
from abc import ABC, abstractmethod


# Определяем абстрактный базовый класс Vehicle
class Vehicle(ABC):
    """Абстрактный базовый класс для всех видов транспортных средств."""

    # Абстрактный метод — его обязательно нужно реализовать в дочерних классах.
    @abstractmethod
    def get_max_speed(self):
        """Возвращает максимальную скорость транспортного средства."""
        pass  # Пока не реализуем — это абстрактный метод

    # Ещё один абстрактный метод
    @abstractmethod
    def get_vehicle_type(self):
        """Возвращает тип транспортного средства (например, 'Автомобиль', 'Велосипед')."""
        pass


# Определяем абстрактный подкласс RoadVehicle — наследуется от Vehicle
class RoadVehicle(Vehicle):
    """Абстрактный класс для транспорта, движущегося по дорогам."""

    # Добавляем ещё один абстрактный метод — его тоже нужно реализовать в потомках
    @abstractmethod
    def get_engine_type(self):
        """Возвращает тип двигателя (например, 'электрический', 'мышечная сила')."""
        pass


# Реализуем конкретный класс Car — наследуется от RoadVehicle
class Car(RoadVehicle):
    """Конкретный класс автомобиля."""

    def __init__(self, тип_кузова, тип_двигателя):
        """
        Конструктор: принимает тип кузова (например, 'седан') и тип двигателя (например, 'электрический').
        Сохраняем их в атрибуты объекта.
        """
        self.тип_кузова = тип_кузова
        self.тип_двигателя = тип_двигателя

    # Реализуем абстрактный метод из Vehicle
    def get_max_speed(self):
        """Для автомобиля максимальная скорость — 200 км/ч (можно изменить)."""
        return 200  # Возвращаем число — максимальную скорость

    # Реализуем абстрактный метод из Vehicle
    def get_vehicle_type(self):
        """Возвращает тип транспорта — всегда 'Автомобиль'."""
        return "Автомобиль"

    # Реализуем абстрактный метод из RoadVehicle
    def get_engine_type(self):
        """Возвращает тип двигателя, который был передан при создании."""
        return self.тип_двигателя


# Реализуем конкретный класс Bicycle — наследуется от RoadVehicle
class Bicycle(RoadVehicle):
    """Конкретный класс велосипеда."""

    def __init__(self, тип_рамы, тип_двигателя="мышечная сила"):
        """
        Конструктор: принимает тип рамы (например, 'горный') и тип двигателя.
        По умолчанию двигатель — 'мышечная сила'.
        """
        self.тип_рамы = тип_рамы
        self.тип_двигателя = тип_двигателя

    # Реализуем абстрактный метод из Vehicle
    def get_max_speed(self):
        """Для велосипеда максимальная скорость — 30 км/ч."""
        return 30

    # Реализуем абстрактный метод из Vehicle
    def get_vehicle_type(self):
        """Возвращает тип транспорта — всегда 'Велосипед'."""
        return "Велосипед"

    # Реализуем абстрактный метод из RoadVehicle
    def get_engine_type(self):
        """Возвращает тип двигателя, который был передан при создании."""
        return self.тип_двигателя


# Функция для демонстрации работы программы
def main():
    """Главная функция — показывает, как работают абстрактные классы и их реализации."""
    print("=== ДЕМОНСТРАЦИЯ РАБОТЫ АБСТРАКТНЫХ КЛАССОВ ===\n")

    # Создаём объект автомобиля
    car = Car("седан", "электрический")
    print(f"Тип транспорта: {car.get_vehicle_type()}")     # Должно вывести: "Автомобиль"
    print(f"Тип двигателя: {car.get_engine_type()}")       # Должно вывести: "электрический"
    print(f"Максимальная скорость: {car.get_max_speed()} км/ч\n")  # Должно вывести: 200

    # Создаём объект велосипеда
    bike = Bicycle("горный")
    print(f"Тип транспорта: {bike.get_vehicle_type()}")     # Должно вывести: "Велосипед"
    print(f"Тип двигателя: {bike.get_engine_type()}")       # Должно вывести: "мышечная сила"
    print(f"Максимальная скорость: {bike.get_max_speed()} км/ч\n")  # Должно вывести: 30

    # Попробуем создать экземпляр абстрактного класса — это вызовет ошибку!
    try:
        v = Vehicle()  # Нельзя создать объект абстрактного класса
    except TypeError as e:
        print(f"Попытка создать Vehicle завершилась ошибкой: {e}")

    # Попробуем создать RoadVehicle — тоже нельзя!
    try:
        rv = RoadVehicle()
    except TypeError as e:
        print(f"Попытка создать RoadVehicle завершилась ошибкой: {e}")

    print("\nВсе тесты пройдены успешно.")


# Запуск программы
if __name__ == "__main__":
    main()