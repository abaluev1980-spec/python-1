# Анализатор чисел
print("=== Анализатор чисел ===")

# Шаг 1: Получаем число от пользователя
while True:
    try:
        N = int(input("Введите целое число больше 0: "))
        if N > 0:
            break
        else:
            print("Ошибка: число должно быть больше 0!")
    except ValueError:
        print("Ошибка: введите целое число!")

print(f"\nАнализируем число {N}:")

# Шаг 2: Находим все делители
deliteli = []  # Создаем пустой список для делителей

# Проверяем все числа от 1 до N
for i in range(1, N + 1):
    if N % i == 0:  # Если делится без остатка
        deliteli.append(i)  # Добавляем в список

print(f"Делители числа {N}: {deliteli}")

# Шаг 3: Проверяем, простое ли число
# Простое число имеет ровно 2 делителя: 1 и само число
if len(deliteli) == 2:
    print(f"Число {N} является простым")
else:
    print(f"Число {N} не является простым")

# Шаг 4: Проверяем, совершенное ли число
# Совершенное число равно сумме всех делителей кроме себя
summa_deliteley = 0
for d in deliteli:
    if d != N:  # Не включаем само число
        summa_deliteley += d

if summa_deliteley == N:
    print(f"Число {N} является совершенным ({'+'.join(str(d) for d in deliteli if d != N)}={N})")
else:
    print(f"Число {N} не является совершенным")